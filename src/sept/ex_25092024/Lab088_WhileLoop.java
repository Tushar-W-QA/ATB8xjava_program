package sept.ex_25092024;


public class Lab088_WhileLoop {

    public static void main(String[] args) {
        // while Loop
        // it is as like a for loop
        // while - A; B; C;
        // A --> Initialisation
        // B --> Conditions
        // C --> Increment/Decrement

        // Syntax:
        int i = 1; // --> A
        while(i<10){ // --> B
            System.out.println(i);
            i++; // --> C
        }

    }

}
