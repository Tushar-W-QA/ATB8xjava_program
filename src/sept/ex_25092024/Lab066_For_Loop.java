package sept.ex_25092024;

public class Lab066_For_Loop {
    public static void main(String[] args) {
        // Loop here to perform a take which is repetitive in nature.
        // Syntax: for( int A; B; C;){
        //         }

        // for --> keyword
        // A   --> Initialisation (From where we wanted to start)
        // B   --> Condition (at which condition to terminate/left the loop)
        // C   --> Increment or decrement (update || wanted to increase or decrease value)

        for (int i = 1; i <= 10; i--) {
            System.out.println(i);
        }
    }
}
