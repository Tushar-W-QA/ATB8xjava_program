package sept.ex_16092024;

public class Lab013_Arithmetic_Operator {
    public static void main(String[] args) {
        // Arithmetic Operator
        // +, -, *, /

        int a = 100;
        int b = 12;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
