package sept.ex_12092024;

public class Lab03_IntergralDT {
    public static void main(String[] args) {
        // data_type variable_name (identifier) = variable_value(literal)
        // Integral datatype are mentioned below:
        // byte, short, int, long, char
        // above type is used depend upon the size-- how much they are using

        byte tushar = 125; // not accepts the value more than 128
        // default value = 0
        System.out.println(tushar);

        short tushar1 = 32767; // not accepts the value more than 32767
        // default value = 0
        System.out.println(tushar1);

        int test_int = 2147483647; // not accepts the value more than 32767
        // default value = 0
        System.out.println(test_int);

        long distance_between_sun_and_moon = 384400000l;
        System.out.println(distance_between_sun_and_moon);

        short s = '$';
        System.out.println(s);

        short a = 'A';
        System.out.println(a);


    }




}
