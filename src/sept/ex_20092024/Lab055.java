package sept.ex_20092024;

public class Lab055 {
    public static void main(String[] args) {
        // Create a switch statement that takes a character
        // input (‘a’, ‘e’, ‘i’, ‘o’, ‘u’) and prints whether it is a vowel or not.

        String word = "j";

        switch(word)
        {
            case "a":
                System.out.println("Vowel");
                break;
            case "e":
                System.out.println("Vowel");
                break;
            case "i":
                System.out.println("Vowel");
                break;
            case "o":
                System.out.println("Vowel");
                break;
            case "u":
                System.out.println("Vowel");
                break;
            default:
                System.out.println("Not a Vowel");
        }

    }
}
